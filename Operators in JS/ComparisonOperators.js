function Operators(){
    var x = 10 ;
    console.log(x==8);       //Equal to
    console.log(x==10);
    console.log(x==="10");  // Equal value and Equal Type
    console.log(x===10);
    console.log(x!=8);     // not equal
    console.log(x!=="10"); // not equal value or not equal type
    console.log(x!==10); 
    console.log(x>8);     // greater than
    console.log(x<8);     // less than
    console.log(x>=10);   // greater than or equal to
    console.log(x<=15);   //less than or equal to
}
Operators();